package org.learningformat.api;

import java.util.Collection;

/**
 * Contains the converted corpus from one input file
 * 
 * @author Peter Palaga
 *
 */
public interface Corpus extends Element {

	/**
	 * @return the name of the corpus
	 */
	public String getSource();

	/**
	 * @param source the name of the corpus
	 */
	public void setSource(String source);
	
	/**
	 * @param the documents in the corpus
	 */
	public Collection<Document> getDocuments();
	
}
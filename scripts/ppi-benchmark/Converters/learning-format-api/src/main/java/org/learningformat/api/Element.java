package org.learningformat.api;

/**
 * A super interface for all other unified format elements.
 * 
 * @author Peter Palaga
 *
 */
public interface Element {

	/**
	 * @return a hierarchical id of the conversion results
	 */
	public String getId();

	/**
	 * @param id a hierarchical id of the conversion results
	 */
	public void setId(String id);
	
	/**
	 * @return the id of the entity in the source corpus.
	 */
	public String getOrigId();

	/**
	 * @param origId
	 *            the id of the entity in the source corpus.
	 */
	public void setOrigId(String origId);

	/**
	 * @return type of the entity.
	 */
	public String getType();

	/**
	 * @param type
	 *            type of the entity.
	 */
	public void setType(String type);


}